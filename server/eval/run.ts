// Eval harness (EPIC-MOAT): measure verdict accuracy against a labeled benchmark.
// Default mode is deterministic and KB-only (no LLM) so it can gate CI; pass --live
// to run the real assessment agent instead. Either way it writes measured numbers to
// kb/EVAL.md — accuracy is reported, not asserted.
import fs from "fs";
import path from "path";
import { CONTROLS, ControlKey, Verdict } from "../src/controls";
import { getFacts } from "../src/kb";

const CONTROL_KEYS = CONTROLS.map((c) => c.key) as ControlKey[];

export interface BenchCase { vendor: string; domain: string; expected: Partial<Record<ControlKey, Verdict>>; }
export type Predict = (domain: string, vendor: string) => Promise<Partial<Record<ControlKey, Verdict>>>;

export interface Metrics {
  total: number; correct: number; accuracy: number; covered: number; coverage: number;
  perControl: Record<string, { correct: number; total: number }>;
}

export async function evaluate(cases: BenchCase[], predict: Predict): Promise<Metrics> {
  let total = 0, correct = 0, covered = 0;
  const perControl: Metrics["perControl"] = {};
  for (const cs of cases) {
    const pred = await predict(cs.domain, cs.vendor);
    for (const k of CONTROL_KEYS) {
      const exp = cs.expected[k];
      if (!exp) continue;
      total++;
      (perControl[k] ??= { correct: 0, total: 0 }).total++;
      const p = pred[k] ?? "unknown";
      if (p !== "unknown") covered++;
      if (p === exp) { correct++; perControl[k].correct++; }
    }
  }
  return { total, correct, accuracy: total ? correct / total : 0, covered, coverage: total ? covered / total : 0, perControl };
}

// KB-only predictor: deterministic, no network.
export const kbPredict: Predict = async (domain) => {
  const { controls } = await getFacts(domain);
  const out: Partial<Record<ControlKey, Verdict>> = {};
  for (const k of CONTROL_KEYS) out[k] = controls[k]?.verdict ?? "unknown";
  return out;
};

export function loadBenchmark(): BenchCase[] {
  return JSON.parse(fs.readFileSync(path.resolve(__dirname, "benchmark.json"), "utf8"));
}

function pct(n: number): string { return (n * 100).toFixed(1) + "%"; }

function report(mode: string, m: Metrics, n: number, stamp: string): string {
  const rows = CONTROL_KEYS.map((k) => {
    const c = m.perControl[k] || { correct: 0, total: 0 };
    return `| ${k} | ${c.correct}/${c.total} | ${pct(c.total ? c.correct / c.total : 0)} |`;
  }).join("\n");
  return `# Eval results

_Generated by \`npm run eval\` (${mode} mode) on ${stamp}. Measured, not asserted — see [server/eval/run.ts](../server/eval/run.ts)._

- **Cases:** ${n} vendors · ${m.total} control labels
- **Overall accuracy:** ${m.correct}/${m.total} = **${pct(m.accuracy)}**
- **KB coverage:** ${m.covered}/${m.total} = ${pct(m.coverage)} (controls with a non-\`unknown\` prediction)

| Control | Correct | Accuracy |
|---|---|---|
${rows}

> Seeded vendors are expected to match (the KB and benchmark draw on the same public
> evidence); uncovered vendors (no \`kb/<domain>.json\`) surface as \`unknown\` and reveal the
> gaps the knowledge base still needs to fill. Accuracy rises as verified facts are added.
`;
}

async function main() {
  const live = process.argv.includes("--live");
  const cases = loadBenchmark();
  let predict = kbPredict;
  let mode = "KB-only";
  if (live) {
    mode = "live";
    const { assessApp } = await import("../src/agent");
    predict = async (domain, vendor) => {
      const r = await assessApp({ name: vendor, url: `https://${domain}` });
      const out: Partial<Record<ControlKey, Verdict>> = {};
      for (const k of CONTROL_KEYS) out[k] = r.capabilities[k]?.verdict ?? "unknown";
      return out;
    };
  }
  const m = await evaluate(cases, predict);
  const stamp = new Date().toISOString().slice(0, 10);
  const md = report(mode, m, cases.length, stamp);
  fs.writeFileSync(path.resolve(__dirname, "..", "..", "kb", "EVAL.md"), md);
  console.log(`[eval] ${mode}: accuracy ${pct(m.accuracy)} (${m.correct}/${m.total}), coverage ${pct(m.coverage)} -> kb/EVAL.md`);
}

if (require.main === module) main();
